
//While loop
// The while loop keeps running as long as the condition is true.
// The condition is checked before running the loop body.

let num=1
while(num<=10){
    //console.log( `The number is ${num}`);
    //num++
}

//Do...While Loop
//The do...while loop will always run at least once, because the condition is checked after the loop body.

let score=0
do{
    console.log(`Score is: ${score}`);
    score=score+2
}
while(score<=20);

